public class ShortComment {
    String shortCommentWriterName;
    String shortCommentWriterUrl;
    String shortCommentTime;
    String shortCommentContent;
    String shortCommentVoteCount;
    String shortCommentWriterPortrait;

    public String getShortCommentWriterName() {
        return shortCommentWriterName;
    }

    public void setShortCommentWriterName(String shortCommentWriterName) {
        this.shortCommentWriterName = shortCommentWriterName;
    }

    public String getShortCommentWriterUrl() {
        return shortCommentWriterUrl;
    }

    public void setShortCommentWriterUrl(String shortCommentWriterUrl) {
        this.shortCommentWriterUrl = shortCommentWriterUrl;
    }

    public String getShortCommentTime() {
        return shortCommentTime;
    }

    public void setShortCommentTime(String shortCommentTime) {
        this.shortCommentTime = shortCommentTime;
    }

    public String getShortCommentContent() {
        return shortCommentContent;
    }

    public void setShortCommentContent(String shortCommentContent) {
        this.shortCommentContent = shortCommentContent;
    }

    public String getShortCommentVoteCount() {
        return shortCommentVoteCount;
    }

    public void setShortCommentVoteCount(String shortCommentVoteCount) {
        this.shortCommentVoteCount = shortCommentVoteCount;
    }

    public String getShortCommentWriterPortrait() {
        return shortCommentWriterPortrait;
    }

    public void setShortCommentWriterPortrait(String shortCommentWriterPortrait) {
        this.shortCommentWriterPortrait = shortCommentWriterPortrait;
    }
}
